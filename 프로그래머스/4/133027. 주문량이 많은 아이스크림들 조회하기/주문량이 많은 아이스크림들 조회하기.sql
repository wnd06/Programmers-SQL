WITH CTE AS (SELECT FLAVOR, SUM(TOTAL_ORDER) AS TOTAL1
FROM FIRST_HALF
GROUP BY FLAVOR
UNION ALL
SELECT FLAVOR, SUM(TOTAL_ORDER) AS TOTAL2
FROM JULY
GROUP BY FLAVOR)

SELECT FLAVOR
FROM CTE
GROUP BY FLAVOR
ORDER BY SUM(TOTAL1) DESC
LIMIT 3


