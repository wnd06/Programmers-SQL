SELECT CASE WHEN (PRICE div 10000) * 10000 <= PRICE THEN (PRICE div 10000) * 10000
            # WHEN PRICE < 20000 THEN 10000
            # WHEN PRICE < 30000 THEN 20000
            # WHEN PRICE < 40000 THEN 30000
        END AS PRICE_GROUP,
        COUNT(*) AS PRODUCTS
FROM PRODUCT 
GROUP BY PRICE_GROUP
ORDER BY PRICE_GROUP